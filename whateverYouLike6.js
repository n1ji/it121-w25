let x = 10;

// TRUE
console.log(x == 10);
console.log(x === 10);
console.log(x != 5);
console.log(x !== 5);
console.log(x > 5);
console.log(x < 20);
console.log(x >= 10);
console.log(x <= 20);
console.log(x && true);
console.log(Boolean(x || 0));
console.log(!false);

// BREAK
console.log("\n")

// FALSE
console.log(x == 5);
console.log(x === '10');
console.log(x != 10);        
console.log(x !== 10);
console.log(x > 20);         
console.log(x < 5);
console.log(x >= 20);
console.log(x <= 5);
console.log(x && false);
console.log((x < 5) || false);
console.log(!true);